    private void upButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upButtonActionPerformed
        int mod = evt.getModifiers();
        if ((mod & ActionEvent.ALT_MASK) == ActionEvent.ALT_MASK){
            boolean setting = true;
            if (xInvertBox.isVisible()) {
                    setting = false;
                }
            xInvertBox.setVisible(setting);
            yInvertBox.setVisible(setting);
            yInterceptInvertBox.setVisible(setting);
            return;
        }
        if ((mod & ActionEvent.CTRL_MASK) == ActionEvent.CTRL_MASK){
            boolean setting = true;
            if (xCheckBox.isVisible()) {
                    setting = false;
                    xCheckBox.setSelected(setting);
                    xCheckBox.setSelected(setting);
                    yInterceptCheckBox.setSelected(setting);
                }
            xCheckBox.setVisible(setting);
            yCheckBox.setVisible(setting);
            yInterceptCheckBox.setVisible(setting);
            return;
        }
        up();
    }//GEN-LAST:event_upButtonActionPerformed

    private void downButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_downButtonActionPerformed
        int mod = evt.getModifiers();
        if ((mod & ActionEvent.ALT_MASK) == ActionEvent.ALT_MASK){
            boolean setting = true;
            if (xInvertBox.isVisible()) {
                    setting = false;
                    /* unselect the boxes when hiding them */
                    xInvertBox.setSelected(setting);
                    yInvertBox.setSelected(setting);
                    yInterceptInvertBox.setSelected(setting);
                }
            xInvertBox.setVisible(setting);

            yInvertBox.setVisible(setting);
            yInterceptInvertBox.setVisible(setting);
            return;
        }
        if ((mod & ActionEvent.CTRL_MASK) == ActionEvent.CTRL_MASK){
            boolean setting = true;
            if (xCheckBox.isVisible()) {
                    setting = false;
                    /* unselect the boxes when hiding them */
                    xCheckBox.setSelected(setting);
                    xCheckBox.setSelected(setting);
                    yInterceptCheckBox.setSelected(setting);
                }
            xCheckBox.setVisible(setting);
            yCheckBox.setVisible(setting);
            yInterceptCheckBox.setVisible(setting);
            return;
        }
        down();
    }//GEN-LAST:event_downButtonActionPerformed

