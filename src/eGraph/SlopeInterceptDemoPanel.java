    private void up(){
        int temp = 0;
        if (yInterceptCheckBox.isSelected()){
            temp = (Integer)yInterceptSpinner.getValue();
            if (yInterceptInvertBox.isSelected()){
                temp--;
            }else temp++;

            yInterceptSpinner.setValue(temp);
        }
        if (xCheckBox.isSelected()){
            temp = (Integer)xSpinner.getValue();
            if (xInvertBox.isSelected()){
                temp--;
            }else temp++;
            xSpinner.setValue(temp);
        }
        if (yCheckBox.isSelected()){
            temp = (Integer)ySpinner.getValue();
            if (yInvertBox.isSelected()){
                temp--;
            }else temp++;

            ySpinner.setValue(temp);
        }
        this.buildGraph();
    }
    private void down(){
        int temp = 0;

        if (yInterceptCheckBox.isSelected()){
            temp = (Integer)yInterceptSpinner.getValue();
            if (yInterceptInvertBox.isSelected()){
                temp++;
            }else temp--;
            yInterceptSpinner.setValue(temp);
        }
        if (xCheckBox.isSelected()){
            temp = (Integer)xSpinner.getValue();
            if (xInvertBox.isSelected()){
                temp++;
            }else temp--;
            xSpinner.setValue(temp);
        }
        if (yCheckBox.isSelected()){
            temp = (Integer)ySpinner.getValue();
            if (yInvertBox.isSelected()){
                temp++;
            }else temp--;
            ySpinner.setValue(temp);
        }
        this.buildGraph();
    }

    public void buildGraph(){
        paperPanel.removeAll(); //I was stacking a new slopeCanvas in each htime the demo was updated, needless to say the tabbedpane got real heavy
        int q = (Integer)quadrantSpinner.getValue();
        int x = (Integer)xSpinner.getValue();
        int y = (Integer)ySpinner.getValue();
        int yi = (Integer)yInterceptSpinner.getValue();

        /* y over x */
        /* Keep our slope "fraction" under control */
        
            if (y < 0 && x < 0) {
                y = y * -1;
                x = x * -1;                
            }//currently have a small issue with the algorithm when 0 over 0 is crossed

            xSpinner.setValue(x);
            ySpinner.setValue(y);
                
        paperPanel.add(new SlopeCanvas(q, numbersBox.isSelected(), ghostCheckBox.isSelected(),
                         lineBox.isSelected(), pointsBox.isSelected(), paperPanel.getSize(), x, y, yi));
        this.validate();
        
    }
   
